#include "../push_swap.h"

void	sb(Pile *b)
{
	int temp;

	if (!b->tab[0] || !b->tab[1])
		return ;
	temp = b->tab[0];
	b->tab[0] = b->tab[1];
	b->tab[1] = temp;
	return ;
}
